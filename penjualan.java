       public void autonumber(){
        try{
        Connection c = koneksi.getKoneksi();
        Statement s = c.createStatement();
        String sql = "SELECT * FROM penjualanpupuk ORDER BY Id_Order DESC";
        ResultSet rs = s.executeQuery(sql); 
        
        if(rs.next()){
            String NoID = rs.getString("Id_Order").substring(2);
            String ID = "" + (Integer.parseInt(NoID)+1);
            String Zero = "";
            
            if (ID.length()==1)
            {Zero = "00";}
            else if (ID.length()==2)
            {Zero = "0";}
            else if (ID.length()==3)
            {Zero = "";}
            
            txtidorder.setText("OR" + Zero + ID);
        }else{
            txtidorder.setText("OR001");
        }
        rs.close();
        s.close();
    }catch(Exception e){
        System.out.println("autonumber error");
    }
    }
