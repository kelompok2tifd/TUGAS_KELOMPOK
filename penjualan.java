    private void txtsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtsimpanActionPerformed
        // TODO add your handling code here:
        String tampilan = "yyyy-MM-dd";
        SimpleDateFormat fm = new SimpleDateFormat(tampilan);
        String tanggal = String.valueOf(fm.format(txttglorder.getDate()));
        String Id_Order = txtidorder.getText();
        String Id_Admin = (String) txtidadmin.getSelectedItem();
        String Id_Pupuk = (String) txtidpupuk.getSelectedItem();
        String Id_Pelanggan = (String) txtidpelanggan.getSelectedItem();
        String Jumlah_Beli = txtjmlbeli.getText();
        String Total = txttotalhrg.getText();
        String Bayar = txtbayar.getText();
        String Kembali = txtkembalian.getText();

        if (Id_Order.equals("") || tanggal.equals("") || Id_Admin.equals("") || Id_Pupuk.equals("") || Jumlah_Beli.equals("") || Total.equals("") || Bayar.equals("") || Kembali.equals("")){
            JOptionPane.showMessageDialog(null, "Isi Data Dengan Lengkap!");
        }else{
            try{
                Connection c = koneksi.getKoneksi();
                String sql = "INSERT INTO penjualanpupuk VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)";
                PreparedStatement p = c.prepareStatement(sql);
                p.setString(1, Id_Order);
                p.setString(2, tanggal);
                p.setString(3, Id_Pelanggan);
                p.setString(4, Id_Admin);
                p.setString(5, Id_Pupuk);
                p.setString(6, Jumlah_Beli);
                p.setString(7, Total);
                p.setString(8, Bayar);
                p.setString(9, Kembali);
                p.executeUpdate();
                p.close();
                JOptionPane.showMessageDialog(null, "Data Ditambahkan");
            } catch (SQLException ex) {
                System.out.println("Error");
            }
            clear();
            tampiltabel();
            tampilcombo();
        }
    }//GEN-LAST:event_txtsimpanActionPerformed

