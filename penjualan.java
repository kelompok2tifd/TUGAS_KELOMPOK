    private void txtdatapenjualanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtdatapenjualanMouseClicked
        // TODO add your handling code here:
        try {
            Connection c = koneksi.getKoneksi();
            int row =txtdatapenjualan.getSelectedRow();
            String tabel_klik=(txtdatapenjualan.getModel().getValueAt(row, 0).toString());
            java.sql.Statement stm = c.createStatement();
            java.sql.ResultSet sql = stm.executeQuery("select * from penjualanpupuk where Id_Order= '"+tabel_klik+"'");
            if(sql.next()){
                String id_order = sql.getString("Id_Order");
                txtidorder.setText(id_order);
                Date tgl_order = sql.getDate("Tanggal_Order");
                txttglorder.setDate(tgl_order);
                String id_admin = sql.getString("Id_Admin");
                txtidadmin.setSelectedItem(id_admin);
                String id_pelanggan = sql.getString("Id_Pelanggan");
                txtidpelanggan.setSelectedItem(id_pelanggan);
                String id_pupuk = sql.getString("Id_Pupuk");
                txtidpupuk.setSelectedItem(id_pupuk);
                String jml_beli = sql.getString("Jumlah_Beli");
                txtjmlbeli.setText(jml_beli);
                String total_hrg = sql.getString("Total_Harga");
                txttotalhrg.setText(total_hrg);
                String bayar = sql.getString("Jumlah_Bayar");
                txtbayar.setText(bayar);
                String kembalian = sql.getString("Kembali");
                txtkembalian.setText(kembalian);
            }
        } catch (Exception e) {}
    }//GEN-LAST:event_txtdatapenjualanMouseClicked

